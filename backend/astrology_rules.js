/**
 * Get the base meaning of a planet (positive or negative)
 * @param {string} planet - Planet name (lowercase: sun, moon, etc.)
 * @param {string} polarity - "positive" or "negative"
 * @returns {object} Planet meaning object for the specified polarity
 */
function getPlanetMeaning(planet, polarity = "positive") {
  const planetData = planetMeanings[planet.toLowerCase()];
  if (!planetData) return null;
  return planetData[polarity] || planetData.positive;
}

/**
 * Get the base meaning of a sign (positive or negative)
 * @param {string} sign - Sign name (e.g., "Aries", "Gemini")
 * @param {string} polarity - "positive" or "negative"
 * @returns {object} Sign meaning object for the specified polarity
 */
function getSignMeaning(sign, polarity = "positive") {
  const signData = signMeanings[sign];
  if (!signData) return null;
  return signData[polarity] || signData.positive;
}

/**
 * Get house meaning (positive or negative)
 * @param {number} house - House number (1-12)
 * @param {string} polarity - "positive" or "negative"
 * @returns {object} House meaning object for the specified polarity
 */
function getHouseMeaning(house, polarity = "positive") {
  const houseData = houseMeanings[house];
  if (!houseData) return null;
  return houseData[polarity] || houseData.positive;
}

/**
 * Get the combined interpretation of a planet in a sign (positive or negative)
 * @param {string} planet - Planet name (lowercase: sun, moon, etc.)
 * @param {string} sign - Sign name (e.g., "Aries", "Gemini")
 * @param {string} polarity - "positive" or "negative"
 * @returns {string} Combined interpretation text
 */
function getPlanetSignInterpretation(planet, sign, polarity = "positive") {
  const planetKey = planet.toLowerCase();
  const signKey = sign;

  if (
    planetSignCombinations[planetKey] &&
    planetSignCombinations[planetKey][signKey] &&
    planetSignCombinations[planetKey][signKey][polarity]
  ) {
    return planetSignCombinations[planetKey][signKey][polarity];
  }

  // Fallback: combine base meanings if specific combination not found
  const planetMeaning = getPlanetMeaning(planet, polarity);
  const signMeaning = getSignMeaning(sign, polarity);

  if (planetMeaning && signMeaning) {
    return `${
      planetMeaning.core
    } expressed through ${signMeaning.core.toLowerCase()}.`;
  }

  return `The ${planet} in ${sign} influences your chart.`;
}

/**
 * Determine polarity based on aspect type
 * @param {string} aspectType - Aspect type (e.g., "conjunction", "opposition", "square", "trine", "sextile")
 * @returns {string} "positive" or "negative"
 */
function getAspectPolarity(aspectType) {
  const positiveAspects = ["trine", "sextile", "conjunction"]; // Conjunction can be either, but often positive
  const negativeAspects = ["opposition", "square"];

  if (positiveAspects.includes(aspectType?.toLowerCase())) {
    return "positive";
  }
  if (negativeAspects.includes(aspectType?.toLowerCase())) {
    return "negative";
  }
  // Default to positive for unknown aspects
  return "positive";
}

